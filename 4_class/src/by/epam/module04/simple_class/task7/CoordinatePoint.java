package by.epam.module04.simple_class.task7;

public class CoordinatePoint {
	
	private double x;
	private double y;
	
	public CoordinatePoint(double x, double y) {
		super();
		this.x = x;
		this.y = y;
	}
	
	public double getX() {
		return this.x;
	}
	
	public double getY() {
		return this.y;
	}



}
